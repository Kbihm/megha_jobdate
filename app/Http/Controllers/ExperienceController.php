    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $id = Auth::user()->id;
        $experiences = Experience::where('employee_id', $id)->get();
        return view('employee.experience.index', compact('experiences'));
    }

